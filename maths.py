import math

pi = 3.14

print("Pi rounded to the nearest int: " + str(round(pi)))
print("Pi rounded up: " + str(math.ceil(pi)))
print("Pi rounded down: " + str(math.floor(pi)))
print("Absolute value of pi: " + str(abs(pi)))
print("Pi to the power of 2: " + str(pow(pi,2)))
print("Square root of 4: " + str(math.sqrt(pi)))

